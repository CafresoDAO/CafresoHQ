// Mirror the repo-root `assets/` directory into `frontend/static/assets/` so the
// SvelteKit build serves every brand/partner image at `/assets/<name>`.
//
// Why this exists: components reference images at `/assets/*.png`, but the image
// files historically lived only at repo-root `assets/` (consumed by the container
// / hq-ui build), never in the frontend's `static/` dir — so they 404'd on the
// asset canister (ai.cafreso.com / v4tdv). This runs as `prebuild`, so it can't
// regress, and dropping real artwork into repo-root `assets/` auto-publishes it
// (overriding any placeholder generated by scripts/gen_placeholder_assets.py).
//
// Zero-dependency, idempotent. Only copies when missing or newer.
import { cpSync, mkdirSync, readdirSync, statSync, existsSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));     // frontend/scripts
const repoRoot = resolve(here, '..', '..');               // repo root
const SRC = join(repoRoot, 'assets');                     // canonical source
const DEST = resolve(here, '..', 'static', 'assets');     // frontend/static/assets

if (!existsSync(SRC)) {
  console.warn(`[sync-static-assets] no source dir at ${SRC} — skipping`);
  process.exit(0);
}
mkdirSync(DEST, { recursive: true });

const IMAGE_RE = /\.(png|jpe?g|svg|webp|gif|ico)$/i;
let copied = 0;
for (const name of readdirSync(SRC)) {
  const from = join(SRC, name);
  if (!statSync(from).isFile() || !IMAGE_RE.test(name)) continue;
  const to = join(DEST, name);
  // Copy when the dest is missing or older than the source.
  if (!existsSync(to) || statSync(from).mtimeMs > statSync(to).mtimeMs) {
    cpSync(from, to);
    copied++;
  }
}
console.log(`[sync-static-assets] ${copied} file(s) synced ${SRC} -> ${DEST}`);
